<p>Imdb-bingo is a bingo game that you play while watching a movie. It uses the <a href="http://imdb.com/">imdb</a> keywords for a movie to create the bingo boards.</p>
<p>Search for the movie you want to watch in the textbox and you'll get a board.</p>
<p> When something that you have on your board happens in the movie, you shout it aloud and mark the corresponding square. The first one to get three marks in a row is wins.</p>
<p><b>Example:</b> Suppose you're watching 2001: A space oddysey and you have the word "sunrise" in one of your squares. In the first scene where the sun comes up you shout "sunrise!" and since no one objects you get to mark the square.</p>
